package abc;

public class Patient {
	    private String name;
	    private int age;
	    private String Issue;

	    public void setName(String n) {
	        name = n;
	    }

	    public void setAge(int a) {
	        age = a;
	    }

	    public void setHealthIssue(String h) {
	        Issue = h;
	    }

	    public String getName() {
	        return name;
	    }

	    public int getAge() {
	        return age;
	    }

	    public String getHealthIssue() {
	        return Issue;
	    }
	}

